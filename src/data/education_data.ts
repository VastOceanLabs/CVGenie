// Education validation rules
export const educationValidation = {
  required: ['institution', 'degree', 'field'], // graduationDate is optional
  optional: ['graduationDate', 'gpa', 'honors', 'coursework', 'location'],
  
  gpaValidation: (gpa, scale = '4.0') => {
    const numericGpa = parseFloat(gpa);
    if (isNaN(numericGpa)) return false;
    
    switch (scale) {
      case '4.0':
        return numericGpa >= 0 && numericGpa <= 4.0;
      case '5.0':
        return numericGpa >= 0 && numericGpa <= 5.0;
      case '100':
        return numericGpa >= 0 && numericGpa <= 100;
      default:
        return true;
    }
  },

  dateValidation: (date) => {
    if (!date) return true; // Optional field
    // Require 4-digit year format
    if (!/^\d{4}$/.test(date)) return false;
    
    const currentYear = new Date().getFullYear();
    const year = parseInt(date, 10);
    return year >= 1950 && year <= currentYear + 6; // Allow future graduation dates
  },

  shouldShowGPA: (level) => {
    const showGPALevels = ['bachelors', 'masters', 'associate', 'doctoral', 'certificate'];
    return showGPALevels.includes(level);
  }
};

