// Helper functions for education section
export const educationHelpers = {
  // Get degree suggestions based on field
  getDegreeSuggestions: (field) => {
    const fieldLower = field.toLowerCase();
    
    if (fieldLower.includes('computer') || fieldLower.includes('software')) {
      return ['Bachelor of Science (BS)', 'Master of Science (MS)', 'Bachelor of Computer Science (BCS)', 'Doctor of Philosophy (PhD)'];
    }
    if (fieldLower.includes('business') || fieldLower.includes('management')) {
      return ['Bachelor of Business Administration (BBA)', 'Master of Business Administration (MBA)', 'Doctor of Business Administration (DBA)'];
    }
    if (fieldLower.includes('engineering')) {
      return ['Bachelor of Engineering (BE)', 'Master of Engineering (ME)', 'Bachelor of Science (BS)', 'Doctor of Philosophy (PhD)'];
    }
    if (fieldLower.includes('art') || fieldLower.includes('design')) {
      return ['Bachelor of Fine Arts (BFA)', 'Master of Fine Arts (MFA)', 'Bachelor of Arts (BA)'];
    }
    if (fieldLower.includes('medicine') || fieldLower.includes('medical')) {
      return ['Doctor of Medicine (MD)', 'Bachelor of Science (BS)', 'Master of Science (MS)'];
    }
    if (fieldLower.includes('law') || fieldLower.includes('legal')) {
      return ['Juris Doctor (JD)', 'Master of Laws (LLM)', 'Bachelor of Arts (BA)'];
    }
    if (fieldLower.includes('phd') || fieldLower.includes('doctorate') || fieldLower.includes('doctoral')) {
      return ['Doctor of Philosophy (PhD)', 'Doctor of Education (EdD)', 'Doctor of Psychology (PsyD)'];
    }
    
    return ['Bachelor of Arts (BA)', 'Bachelor of Science (BS)', 'Master of Arts (MA)', 'Master of Science (MS)', 'Doctor of Philosophy (PhD)'];
  },

  // Format graduation date
  formatGraduationDate: (date, current) => {
    if (current) return 'Expected ' + date;
    return date;
  },

  // Get institution type from name
  getInstitutionType: (institutionName) => {
    const name = institutionName.toLowerCase();
    
    if (name.includes('university')) return 'University';
    if (name.includes('college')) return 'College';
    if (name.includes('institute')) return 'Institute';
    if (name.includes('school')) return 'School';
    if (name.includes('academy')) return 'Academy';
    
    return 'Institution';
  },

  // Validate education entry completeness
  validateEducationEntry: (education) => {
    const errors = [];
    
    if (!education.institution?.trim()) errors.push('Institution name is required');
    if (!education.degree?.trim()) errors.push('Degree type is required');
    if (!education.field?.trim()) errors.push('Field of study is required');
    
    // Graduation date is optional - only validate if provided
    if (education.graduationDate && !educationValidation.dateValidation(education.graduationDate)) {
      errors.push('Invalid graduation year (must be 4-digit year between 1950 and ' + (new Date().getFullYear() + 6) + ')');
    }
    
    // Use dynamic scale validation
    if (education.gpa && !educationValidation.gpaValidation(education.gpa, education.gpaScale || '4.0')) {
      errors.push(`GPA must be valid for a ${education.gpaScale || '4.0'} scale`);
    }
    
    return {
      isValid: errors.length === 0,
      errors
    };
  }
};

